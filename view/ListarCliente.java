    public void readJTable(){
    	DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    	
    	ClienteDAO clientedao = new ClienteDAO();
    	
    	for(Cliente c: clientedao.listar()){
    		modelo.addRow(new Object[]{
       				c.getCpf(),
    				c.getNomeCli(),
    				c.getTelefone(),
    				c.getEmail()
    		});
    		
    	}
    }

